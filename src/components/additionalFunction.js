export function getNamesProducts(array) {
    let i = 0;
    const nameProducts = [];
    array.map((item, index) => {
        item.map((product, number) => {
            nameProducts[i] = {
                name: product.nameProduct,
                arrayN: index,
                elemN: number
            };
            i++
        })
    })
    return nameProducts;
}